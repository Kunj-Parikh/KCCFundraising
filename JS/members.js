const img = document.querySelector('#labcoats')

window.addEventListener('scroll', function() {
    const FACTOR = -0.000075
    console.log('scrolling: ' + scrollY)
    if (scrollY * (FACTOR) + 1 < 0 || scrollY * (FACTOR) + 1 > 1) return
    img.setAttribute('style', `transform: scale(${scrollY*(FACTOR)+1}); width: 100%`)
    
})

